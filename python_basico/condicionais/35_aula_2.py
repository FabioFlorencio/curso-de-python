condicao1 = True
condicao2 = False
condicao3 = False
condicao4 = False 

if condicao1:
    print(f'Código para condição 1. {condicao1=}')
elif condicao2:
    print(f'Código para condição 2. {condicao2=}')    
elif condicao3:
    print(f'Código para condição 3. {condicao3=}')
elif condicao4:
    print(f'Código para condição 4. {condicao4=}')    
else:
    print(f'Nenhuma condição foi satisfeita')    


