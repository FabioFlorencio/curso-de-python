'''
    Exemplo usando for com a função reversed()
    função reversed() em Python retorna um iterador que acessa os elementos de uma sequência em ordem inversa. 
    Vamos ver um exemplo de como usar reversed() dentro de uma estrutura de repetição for.

'''

# Definindo uma lista de números
numeros = [1, 2, 3, 4, 5]

# Usando reversed() com a estrutura de repetição for
for numero in reversed(numeros):
    print(numero)


invert = reversed(numeros)    

print(invert)












